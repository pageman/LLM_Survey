def main() -> None:
    repo_root = Path(__file__).resolve().parents[1]
    generated = repo_root / "artifacts" / "generated"
    generated.mkdir(parents=True, exist_ok=True)

    executed = []
    for script in SCRIPTS:
        subprocess.run([sys.executable, script], cwd=repo_root, check=True, capture_output=True, text=True)
        executed.append(script)

    report = build_report(
        experiment_id="all_local_demos",
        module="experiments.runner",
        metrics={"num_executed": len(executed)},
        artifacts={"scripts": executed},
        notes=["Runs all local demos and regenerates all JSON reports."],
    )
    write_report(report, generated / "all_local_demos.json")
    print(json.dumps(report, indent=2))


if __name__ == "__main__":
    main()
